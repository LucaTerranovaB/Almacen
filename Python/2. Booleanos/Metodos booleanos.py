cadena = "Estoy mostrando metodos booleanos para los strings"

# Metodo Startswith (Verifica con que comienza la cadena)

print(cadena.startswith("e"))

#Metodo endswith (Verifica con que termina la cadena)

print(cadena.endswith("a"))

#Metodo isupper (Verifica si la cadena entera esta en mayuscula)

print(cadena.isupper())

#Metodo islower (Verifica si la cadena entera esta en minuscula)

print(cadena.islower())
